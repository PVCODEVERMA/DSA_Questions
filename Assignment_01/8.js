//Write a code to convert a given string to uppercase or lowercase.

function toUpperCase(str){
    return str.toUpperCase();
}
function toLowerCase(str){
    return str.toLowerCase();
}
let inputString1 = 'hello,world';
let inputString2 = 'hello,world';

let result_toUppercase_String = toUpperCase(inputString1);
let result_toLowerCase_String = toLowerCase(inputString2);

console.log(result_toUppercase_String);
console.log(result_toLowerCase_String);
